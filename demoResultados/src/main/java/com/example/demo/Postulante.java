package com.example.demo;

public class Postulante {
	public String codigo_categoria;
	public String nombre_categoria;
	public String codigo_agrupacion;
	public String nombre_agrupacion;
	public String codigo_lista;
	public String nombre_lista;
	public Postulante(String codigo_categoria, String nombre_categoria, String codigo_agrupacion, String nombre_agrupacion, String codigo_lista,
			String nombre_lista) {
		super();
		this.codigo_categoria = codigo_categoria;
		this.nombre_categoria = nombre_categoria;
		this.codigo_agrupacion = codigo_agrupacion;
		this.nombre_agrupacion = nombre_agrupacion;
		this.codigo_lista = codigo_lista;
		this.nombre_lista = nombre_lista;
	}
	
	
}
